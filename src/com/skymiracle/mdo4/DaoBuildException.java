package com.skymiracle.mdo4;

public class DaoBuildException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5669744052451710243L;

	public DaoBuildException() {
		super();
	}

	public DaoBuildException(String message) {
		super(message);
	}

	public DaoBuildException(String message, Throwable cause) {
		super(message, cause);
	}

	public DaoBuildException(Throwable cause) {
		super(cause);
	}
}
